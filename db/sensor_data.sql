CREATE TABLE sensor_data (
    id INT AUTO_INCREMENT PRIMARY KEY,
    voltage FLOAT DEFAULT NULL,
    currentA FLOAT DEFAULT NULL,
    currentB FLOAT DEFAULT NULL,
    currentC FLOAT DEFAULT NULL,
    powerA FLOAT DEFAULT NULL,
    powerB FLOAT DEFAULT NULL,
    powerC FLOAT DEFAULT NULL,
    totalPower FLOAT DEFAULT NULL,
    reactivePowerA FLOAT DEFAULT NULL,
    reactivePowerB FLOAT DEFAULT NULL,
    reactivePowerC FLOAT DEFAULT NULL,
    totalReactivePower FLOAT DEFAULT NULL,
    powerFactorA FLOAT DEFAULT NULL,
    powerFactorB FLOAT DEFAULT NULL,
    powerFactorC FLOAT DEFAULT NULL,
    totalPowerFactor FLOAT DEFAULT NULL,
    apparentPowerA FLOAT DEFAULT NULL,
    apparentPowerB FLOAT DEFAULT NULL,
    apparentPowerC FLOAT DEFAULT NULL,
    totalApparentPower FLOAT DEFAULT NULL,
    frequency FLOAT DEFAULT NULL,
    energy FLOAT DEFAULT NULL,
    angleA FLOAT DEFAULT NULL,
    angleB FLOAT DEFAULT NULL,
    angleC FLOAT DEFAULT NULL,
    totalAngle FLOAT DEFAULT NULL,
    timestamp TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);
